export function DeprecatedLanguagePanel() {
  const { currentLanguage, languages, setLanguage, t } = useI18n();
  return (
    <div style={{ padding: 14, display: "flex", flexDirection: "column", gap: 12 }}>
      <div>
        <div style={{ fontSize: 14, fontWeight: 650, color: "var(--text-1)" }}>{t("language.panel.title")}</div>
        <div style={{ marginTop: 4, fontSize: 12, color: "var(--text-3)", lineHeight: 1.35 }}>{t("language.panel.subtitle")}</div>
      </div>
      <div style={{ display: "flex", flexDirection: "column", gap: 6 }}>
        {languages.map((language) => (
          <button
            key={language.locale}
            type="button"
            onClick={() => { setLanguage(language.locale).catch(console.warn); }}
            style={{
              display: "flex",
              alignItems: "center",
              justifyContent: "space-between",
              gap: 10,
              padding: "9px 10px",
              borderRadius: 6,
              border: language.locale === currentLanguage ? "1px solid var(--accent)" : "1px solid var(--border)",
              background: language.locale === currentLanguage ? "var(--bg-hover)" : "var(--bg-2)",
              color: "var(--text-1)",
              cursor: "pointer",
              textAlign: "left",
            }}
          >
            <span>
              <span style={{ display: "block", fontSize: 13 }}>{language.nativeLabel ?? language.label}</span>
              <span style={{ display: "block", marginTop: 2, fontSize: 11, color: "var(--text-3)" }}>{language.label} · {language.locale}</span>
            </span>
            {language.locale === currentLanguage && (
              <span style={{ fontSize: 11, color: "var(--accent)" }}>{t("language.panel.active")}</span>
            )}
          </button>
        ))}
      </div>
    </div>
  );
}

