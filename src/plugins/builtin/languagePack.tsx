let domTranslatorDisposer: (() => void) | null = null;

function shouldSkipDomTranslation(node: Node): boolean {
  const element = node.nodeType === Node.ELEMENT_NODE ? node as Element : node.parentElement;
  return !!element?.closest("script,style,code,pre,kbd,textarea,input,select,[data-i18n-skip]");
}

function translateDomString(value: string, locale: string): string {
  if (locale === "en") return value;
  const trimmed = value.trim();
  if (!trimmed) return value;
  const key = DOM_TRANSLATION_KEYS[trimmed];
  const direct = DOM_DIRECT[locale]?.[trimmed];
  const translated = direct ?? (key ? translate(key) : "");
  if (!translated || translated === key || translated === trimmed) return value;
  return value.replace(trimmed, translated);
}

function translateDom(root: ParentNode = document): void {
  const locale = getCurrentLanguage();
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT);
  const textNodes: Text[] = [];
  while (walker.nextNode()) textNodes.push(walker.currentNode as Text);
  for (const node of textNodes) {
    if (shouldSkipDomTranslation(node)) continue;
    const original = node.parentElement?.getAttribute("data-i18n-original-text") ?? node.nodeValue ?? "";
    const next = translateDomString(original, locale);
    if (next !== node.nodeValue) {
      node.parentElement?.setAttribute("data-i18n-original-text", original);
      node.nodeValue = next;
    }
  }
  const attrElements = root instanceof Element ? [root, ...Array.from(root.querySelectorAll("[title],[aria-label],[placeholder]"))] : Array.from(document.querySelectorAll("[title],[aria-label],[placeholder]"));
  for (const element of attrElements) {
    if (shouldSkipDomTranslation(element)) continue;
    for (const attr of ["title", "aria-label", "placeholder"]) {
      const value = element.getAttribute(attr);
      if (!value) continue;
      const originalAttr = `data-i18n-original-${attr}`;
      const original = element.getAttribute(originalAttr) ?? value;
      const next = translateDomString(original, locale);
      if (next !== value) {
        element.setAttribute(originalAttr, original);
        element.setAttribute(attr, next);
      }
    }
  }
}

function startDomTranslator(): () => void {
  if (typeof document === "undefined") return () => {};
  let scheduled = false;
  const schedule = () => {
    if (scheduled) return;
    scheduled = true;
    requestAnimationFrame(() => {
      scheduled = false;
      translateDom();
    });
  };
  const observer = new MutationObserver(schedule);
  observer.observe(document.body, { childList: true, subtree: true, characterData: true, attributes: true, attributeFilter: ["title", "aria-label", "placeholder"] });
  const unsubscribe = subscribeI18n(schedule);
  schedule();
  return () => {
    observer.disconnect();
    unsubscribe();
  };
}

